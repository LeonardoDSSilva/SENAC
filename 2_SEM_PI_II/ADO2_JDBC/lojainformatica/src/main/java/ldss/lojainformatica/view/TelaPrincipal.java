    public static void carregarTabela(ArrayList<Computador> listaComputadores) {

        DefaultTableModel modelo = (DefaultTableModel) tbl_Computadores.getModel();
        modelo.setNumRows(0);

        for (Computador computador : listaComputadores) {
            modelo.addRow(new String[]{
                String.valueOf(computador.getIdComputador()),
                String.valueOf(computador.getMarca()),
                String.valueOf(computador.getHd()),
                String.valueOf(computador.getProcessador())
            });
        }
    }

    private void editarComputador(int linhaSelecionada) {
        if (linhaSelecionada != -1) {
            objComputador = new Computador();
            objComputador.setMarca(tbl_Computadores.getValueAt(linhaSelecionada, 1).toString());
            objComputador.setHd(tbl_Computadores.getValueAt(linhaSelecionada, 2).toString());
            objComputador.setProcessador(tbl_Computadores.getValueAt(linhaSelecionada, 3).toString());
            objComputador.setIdComputador(Integer.parseInt(tbl_Computadores.getValueAt(linhaSelecionada, 0).toString()));

            TelaCadastro telaCadastro = new TelaCadastro(objComputador);
            telaCadastro.setVisible(true);
            telaCadastro.setAlwaysOnTop(rootPaneCheckingEnabled);
        } else {
            JOptionPane.showMessageDialog(this, "SELECIONE UMA LINHA", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }


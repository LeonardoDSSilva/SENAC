    private void btn_ApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ApagarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tbl_Computadores.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente apagar o computador selecionado?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (opcao == JOptionPane.YES_OPTION) {

                int idComputador = Integer.parseInt(tbl_Computadores.getValueAt(linhaSelecionada, 0).toString());
                boolean retorno = ComputadorDAO.apagarComputador(idComputador);
                if (retorno) {
                    JOptionPane.showMessageDialog(this, "Computador apagado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                    carregarTabela(ComputadorDAO.listarComputadores());
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao apagar o computador!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um computador para apagar!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_ApagarActionPerformed

    private void cmb_ProcessadorItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_ProcessadorItemStateChanged
        // TODO add your handling code here:
        String processador = cmb_Processador.getSelectedItem().toString();
        if (processador.equals("TODOS")) {
            carregarTabela(ComputadorDAO.listarComputadores());
        } else {
            carregarTabela(ComputadorDAO.listarComputadoresPorProcessador(processador));
        }
    }//GEN-LAST:event_cmb_ProcessadorItemStateChanged

    private void txt_MarcaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_MarcaKeyTyped
        // TODO add your handling code here:
        String marca = txt_Marca.getText();
        if (marca.equals("")) {
            carregarTabela(ComputadorDAO.listarComputadores());
        } else {
            carregarTabela(ComputadorDAO.listarComputadoresPorMarca(marca));
        }
    }//GEN-LAST:event_txt_MarcaKeyTyped

